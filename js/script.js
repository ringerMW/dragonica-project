$(document).ready(function() {
  $('.btn_menu').click(function() {
      $('.header_nav', ).toggleClass('active');
      $('.bgr_hide_menu', ).toggleClass('active');
      $('.subheader_wrap', ).toggleClass('active');
  });
});

$(document).ready(function() {
  $('.btn_nav_close').click(function() {
    $('.header_nav', ).toggleClass('active');
    $('.bgr_hide_menu', ).toggleClass('active');
    $('.subheader_wrap', ).toggleClass('active');
  });
});